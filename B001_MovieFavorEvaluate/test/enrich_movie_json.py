def enrich_positive_movies():
    movies = read_json(POSITIVE_FILE, [])
    for movie in movies:
        if "original_score" not in movie:
            movie["original_score"] = movie.get("score")
        movie["score"] = 100
        movie["target_score"] = 100
        reason = movie.get("reason") or "这部电影对你的审美是正向锚点。"
        movie["core_reason"] = f"核心肯定点：{reason}"
    write_json(POSITIVE_FILE, movies)
    return len(movies)


def enrich_negative_movies():
    movies = read_json(NEGATIVE_FILE, [])
    for movie in movies:
        if "original_score" not in movie:
            movie["original_score"] = movie.get("score")
        target_score = movie.get("target_score", 40)
        try:
            target_score = int(float(target_score))
        except (TypeError, ValueError):
            target_score = 40
        target_score = max(0, min(49, target_score))
        movie["score"] = target_score
        movie["target_score"] = target_score
        movie["core_reason"] = NEGATIVE_CORE_REASONS.get(
            movie.get("title"),
            "核心否定点：这部电影被放入反向库，说明它虽然可能有完成度或声誉，但存在你明确接受不了的气质、套路或表达方式。",
        )
    write_json(NEGATIVE_FILE, movies)
    return len(movies)


def main():
    pos_count = enrich_positive_movies()
    neg_count = enrich_negative_movies()
    print(f"enriched {POSITIVE_FILE}: {pos_count} movies")
    print(f"enriched {NEGATIVE_FILE}: {neg_count} movies")


if __name__ == "__main__":
    main()
